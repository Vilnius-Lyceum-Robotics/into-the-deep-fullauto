@Config
public class IntakeSpecimen extends SequentialCommandGroup {
    public static int specimenIntakePrepTimeout = 500;

    public IntakeSpecimen(Point toSpecimenIntake) {
        addCommands(
                new WaitCommand(specimenIntakePrepTimeout),
                new ParallelRaceGroup(
                        new FollowPath(180, toSpecimenIntake),
                        new WaitCommand(500)
                ),
                new WaitCommand(100),
                new SetClawState(ClawConfiguration.GripperState.CLOSED),
                new WaitCommand(100)
        );
    }
}
